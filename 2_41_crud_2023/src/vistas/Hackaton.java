    void listar(){
   String sql = "SELECT * FROM nombre";
   try{
   cn = con.getConection();
   st = cn.createStatement();
   rs = st.executeQuery(sql);
   
   Object[]nombre = new Object[6];
   modelo= (DefaultTableModel) TablaDatos.getModel();
   while(rs.next()){
   nombre[0] = rs.getInt("ID");
   nombre[1] = rs.getString("Nombre");
   nombre[2] = rs.getString("Apellido");
   nombre[3] = rs.getString("Direccion");          
   nombre[4] = rs.getInt("Edad");
   nombre[5] = rs.getString("Dni");
   modelo.addRow(nombre);
           }
   TablaDatos.setModel(modelo);
  
   }catch(Exception e) {
   
       System.out.println("Este es el error de listar: "+ e); 
   }
    
    }
    
