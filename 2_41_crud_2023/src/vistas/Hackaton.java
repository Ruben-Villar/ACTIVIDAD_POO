    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        int fila = TablaDatos.getSelectedRow();
        if(fila == - 1){
            System.out.println("No se selecciono");
        }else{
            id = Integer.parseInt((String)TablaDatos.getValueAt(fila, 0).toString());
            String nom =(String)TablaDatos.getValueAt(fila, 1);
            String ape =(String)TablaDatos.getValueAt(fila, 2);
            String direc =(String)TablaDatos.getValueAt(fila, 3);
            int edad = Integer.parseInt((String)TablaDatos.getValueAt(fila, 4).toString());
            String dni = (String)TablaDatos.getValueAt(fila, 5);
            
            txtid.setText(" "+ id);
            txtnom.setText(nom);
            txtape.setText(ape);
            txtdirec.setText(direc);
            txtedad.setText(" " + edad);
            txtdni.setText(dni);
            
        }
    }//GEN-LAST:event_TablaDatosMouseClicked

